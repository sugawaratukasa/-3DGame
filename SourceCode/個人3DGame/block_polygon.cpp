CBlock_Polygon::~CBlock_Polygon()
{
}
//******************************************************************************
// 生成関数
//******************************************************************************
CBlock_Polygon * CBlock_Polygon::Create(D3DXVECTOR3 pos, D3DXVECTOR3 rot, D3DXVECTOR3 size, D3DXCOLOR col)
{
	// CPolygonのポインタ
	CBlock_Polygon *pPolygon;

	// メモリ確保
	pPolygon = new CBlock_Polygon;

	// 情報設定
	pPolygon->SetPolygon(pos, rot, size, col);

	// 初期化
	pPolygon->Init();

	// ポインタを返す
	return pPolygon;
}
//******************************************************************************
// 初期化関数
//******************************************************************************
HRESULT CBlock_Polygon::Init(void)
{
	// 初期化
	CScene3d::Init();

	return S_OK;
}
//******************************************************************************
// 終了関数
//******************************************************************************
void CBlock_Polygon::Uninit(void)
{
	// 終了
	CScene3d::Uninit();
}
//******************************************************************************
// 更新関数
//******************************************************************************
void CBlock_Polygon::Update(void)
{
	// 更新
	CScene3d::Update();

	// 位置座標取得
	D3DXVECTOR3 pos = GetPosition();

	// 位置更新
	SetPosition(pos);
}
